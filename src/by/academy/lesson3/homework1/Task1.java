package by.academy.lesson3.homework1;

public class Task1 {

    public static void main(String[] args) {
        double a = 8, b = 5;
        double c;

        c = a + b;
        System.out.println(a + " + " + b + " = " + c);
        c = a - b;
        System.out.println(a + " - " + b + " = " + c);
        c  = a / b;
        System.out.println(a + " / " + b + " = " + c);
        c = a * b;
        System.out.println(a + " * " + b + " = " + c);
    }
}
